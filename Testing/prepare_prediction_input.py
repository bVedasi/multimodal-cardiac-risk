"""Create a prediction-ready sample from the Testing PTB-XL subset.

This script reads a record from the Testing folder, converts the ECG waveform
from .dat/.hea into a model-ready array, and exports a JSON or NPZ file with:
- ecg: 2D array, shape [N, 12] or [12, N]
- tab: 1D clinical feature vector
- scp: 1D raw SCP-code vector
- labels: optional 1D diagnostic label vector of length 5

By default it uses the full PTB-XL dataset folder as a reference so the tabular
and SCP encodings follow the same feature logic as the training pipeline.
"""

from __future__ import annotations

import argparse
import ast
import json
from pathlib import Path
from typing import Dict, List, Tuple

import numpy as np
import pandas as pd

try:
    import wfdb
except ImportError as exc:  # pragma: no cover
    raise ImportError("wfdb is required to read PTB-XL ECG records.") from exc

from sklearn.preprocessing import MultiLabelBinarizer, StandardScaler

TABULAR_NUMERIC_COLS = ["age", "height", "weight"]
TABULAR_CATEGORICAL_COLS = ["sex", "site", "device", "nurse", "heart_axis", "infarction_stadium1", "infarction_stadium2"]
TABULAR_BINARY_COLS = [
    "second_opinion",
    "initial_autogenerated_report",
    "validated_by_human",
    "baseline_drift",
    "static_noise",
    "burst_noise",
    "electrodes_problems",
    "extra_beats",
    "pacemaker",
]


def normalize_category(value: object) -> str:
    if pd.isna(value):
        return "missing"
    text = str(value).strip()
    return text if text and text.lower() != "nan" else "missing"


def normalize_binary(value: object) -> int:
    if pd.isna(value):
        return 0
    text = str(value).strip().lower()
    if not text or text == "nan":
        return 0
    return 1


def load_dataframe(base_dir: Path) -> pd.DataFrame:
    df = pd.read_csv(base_dir / "ptbxl_database.csv", index_col="ecg_id")
    df.scp_codes = df.scp_codes.apply(lambda x: ast.literal_eval(x))
    return df


def load_reference_tables(reference_dir: Path) -> Tuple[pd.DataFrame, pd.DataFrame]:
    ref_df = load_dataframe(reference_dir)
    ref_scp = pd.read_csv(reference_dir / "scp_statements.csv", index_col=0)
    return ref_df, ref_scp


def aggregate_diagnostic_codes(scp_codes: Dict[str, float], ref_scp: pd.DataFrame) -> List[str]:
    diagnostic = ref_scp[ref_scp.diagnostic == 1]
    labels = []
    for code in scp_codes.keys():
        if code in diagnostic.index:
            labels.append(str(diagnostic.loc[code].diagnostic_class))
    return sorted(set(labels))


def build_tabular_vector(row: pd.Series, reference_df: pd.DataFrame) -> np.ndarray:
    numeric_df = reference_df[TABULAR_NUMERIC_COLS].copy()
    for col in TABULAR_NUMERIC_COLS:
        numeric_df[col] = pd.to_numeric(numeric_df[col], errors="coerce")
        numeric_df[col] = numeric_df[col].fillna(numeric_df[col].median())

    scaler = StandardScaler().fit(numeric_df)

    numeric_row = pd.DataFrame([row[TABULAR_NUMERIC_COLS]], columns=TABULAR_NUMERIC_COLS)
    numeric_row = numeric_row.apply(pd.to_numeric, errors="coerce")
    numeric_row = numeric_row.fillna(numeric_df.median())
    numeric_scaled = scaler.transform(numeric_row)[0]

    cat_parts = []
    for col in TABULAR_CATEGORICAL_COLS:
        categories = sorted({normalize_category(value) for value in reference_df[col].tolist()})
        value = normalize_category(row[col])
        one_hot = [1.0 if category == value else 0.0 for category in categories]
        cat_parts.extend(one_hot)

    binary_parts = [float(normalize_binary(row[col])) for col in TABULAR_BINARY_COLS]
    return np.asarray(list(numeric_scaled) + cat_parts + binary_parts, dtype=np.float32)


def build_scp_vector(df: pd.DataFrame, row: pd.Series) -> np.ndarray:
    scp_lists = df.scp_codes.apply(lambda d: [k for k in d.keys()]).tolist()
    mlb = MultiLabelBinarizer()
    mlb.fit(scp_lists)
    return mlb.transform([[k for k in row.scp_codes.keys()]])[0].astype(np.float32)


def build_label_vector(row: pd.Series, reference_scp: pd.DataFrame) -> np.ndarray:
    classes = ["CD", "HYP", "MI", "NORM", "STTC"]
    labels = aggregate_diagnostic_codes(row.scp_codes, reference_scp)
    return np.asarray([1.0 if cls in labels else 0.0 for cls in classes], dtype=np.float32)


def load_ecg(record_base: Path) -> np.ndarray:
    if not record_base.with_suffix(".hea").exists():
        root_candidate = record_base.parents[2] / record_base.name if len(record_base.parents) > 2 else record_base
        if root_candidate.with_suffix(".hea").exists():
            record_base = root_candidate

    signal, _ = wfdb.rdsamp(str(record_base))
    # Keep the ECG as [N, 12] for compatibility with the prediction script.
    return signal.astype(np.float32)


def choose_record(df: pd.DataFrame, ecg_id: int | None) -> pd.Series:
    if ecg_id is None:
        return df.iloc[0]
    if ecg_id not in df.index:
        raise KeyError(f"ecg_id {ecg_id} not found in ptbxl_database.csv")
    return df.loc[ecg_id]


def export_sample(sample: Dict[str, object], output_path: Path) -> None:
    output_path.parent.mkdir(parents=True, exist_ok=True)
    if output_path.suffix.lower() == ".npz":
        np.savez_compressed(output_path, **sample)
    else:
        with open(output_path, "w", encoding="utf-8") as fh:
            json.dump({key: value.tolist() if hasattr(value, "tolist") else value for key, value in sample.items()}, fh, indent=2)


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Create a prediction-ready PTB-XL sample from Testing/")
    parser.add_argument("--testing-dir", default=str(Path(__file__).resolve().parent), help="Path to the Testing folder.")
    parser.add_argument("--reference-dir", default=str(Path(__file__).resolve().parent.parent / "ptb-xl-a-large-publicly-available-electrocardiography-dataset-1.0.3"), help="Reference full PTB-XL dataset folder used to match training preprocessing.")
    parser.add_argument("--ecg-id", type=int, default=None, help="ecg_id to export. Defaults to the first row in the CSV.")
    parser.add_argument("--output", default=str(Path(__file__).resolve().parent / "sample_input.json"), help="Output JSON or NPZ path.")
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    testing_dir = Path(args.testing_dir)
    reference_dir = Path(args.reference_dir)
    output_path = Path(args.output)

    testing_df = load_dataframe(testing_dir)
    reference_df, reference_scp = load_reference_tables(reference_dir)
    row = choose_record(testing_df, args.ecg_id)

    ecg_path = testing_dir / str(row.filename_lr)
    ecg = load_ecg(ecg_path)
    tab = build_tabular_vector(row, reference_df)
    scp = build_scp_vector(reference_df, row)
    labels = build_label_vector(row, reference_scp)

    sample = {
        "ecg": ecg,
        "tab": tab,
        "scp": scp,
        "labels": labels,
    }
    export_sample(sample, output_path)

    print(f"Saved prediction sample to: {output_path}")
    print(f"ECG shape: {ecg.shape}")
    print(f"Tab shape: {tab.shape}")
    print(f"SCP shape: {scp.shape}")
    print(f"Labels: {labels.tolist()}")
    print(f"Record: ecg_id={row.name}, filename={row.filename_lr}")


if __name__ == "__main__":
    main()
